all_data = {}

for i in range(0, len(all_tickers), batch_size):
    batch = all_tickers[i:i+batch_size]
    batch_num = i // batch_size + 1
    total_batches = (len(all_tickers) + batch_size - 1) // batch_size
    print(f"  Batch {batch_num}/{total_batches}: {batch[0]} ... {batch[-1]} ({len(batch)} stocks)")
    
    try:
        data = yf.download(batch, start=start_date, end=end_date, progress=False, threads=True)
        
        if isinstance(data.columns, pd.MultiIndex):
            data.columns = data.columns.get_level_values(0)
        
        for ticker in batch:
            try:
                if 'Close' in data.columns:
                    col = data['Close'][ticker] if ticker in data['Close'].columns else None
                elif ticker in data.columns:
                    col = data[ticker]['Close'] if 'Close' in data[ticker].columns else None
                else:
                    col = None
                
                if col is not None and not col.dropna().empty:
                    all_data[ticker] = col.dropna()
            except:
                pass
    except Exception as e:
        print(f"    ERROR batch: {e}")
        continue

